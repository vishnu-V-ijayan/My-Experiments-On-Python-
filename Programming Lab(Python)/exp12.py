#program to find the largest among three numbers using max function
digit1=int(input("Enter the first number   :   "))
digit2=int(input("Enter the second number   :   "))
digit3=int(input("Enter the third number   :   "))
largest=max(digit1,digit2,digit3) #using max function to generate the largest number
print("The largest among the three numbers is  :  ",largest)